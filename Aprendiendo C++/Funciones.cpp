#include<iostream>
using namespace std;

int main(){
    int entero = 2; //El int se utiliza para los números enteros no decimales
    float decimal = 10.45; //El float se utiliza para los números decimales y enteros
    double doble = 123.435; //El double se utiliza para almacenar numeros mas grandes y en un formato de x64
    char caracter = 'A'; //El char se utiliza para almacenar 1 solo caracter mas no se puede

    cout<<entero<<endl;
    cout<<decimal<<endl;
    cout<<doble<<endl;
    cout<<caracter<<endl;

    return 0;
}